#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*tmp;

	tmp = (char *)s;
	while (*tmp != c && *tmp)
		tmp++;
	if (*tmp != c)
		return (NULL);
	return (tmp);
}
